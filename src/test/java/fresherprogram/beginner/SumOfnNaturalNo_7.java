package fresherprogram.beginner;

public class SumOfnNaturalNo_7
{
    public static void main(String[] args) {
        // let's need to add natural no till 10
        // Natural no start with 1 and end at ...
        int sum = 0;
        for(int i=1;i<=10;i++)
        {
            sum = sum+i;
        }
        System.out.println("Sum of Natural no "+sum);
    }
}
